    <h1>Adding CSS style</h1>

    <p>CSS (Cascading Style Sheets) is a declarative language that we use to alter the appearance of our HTML pages. A CSS file (known as a
      <strong>stylesheet</strong>) consists of a set of formatting rules. The browser interprets and applies these rules when it renders your web page.</p>
    <p>CSS is immensely powerful and flexible. You can control nearly every aspect of an element's appearance, including its overall placement on the page. To give you some idea of just how much control you have, check out the
      <a href="http://www.mezzoblue.com/zengarden/alldesigns/">examples in the CSS Zen Garden</a>. Every one of those examples uses the exact same HTML page, but they all look completely different because each one uses a different CSS stylesheet.</p>
    <h2 id="sec-whytwodifferentlanguages">Why Two Different Languages?</h2>
    <p>If you are new to web programming, you might be wondering why there are two different languages: HTML for your page content; and CSS for your formatting rules. Why not just include the formatting right in with the content?</p>
    <p>There is an old, tried-and-true principle of programming that is known as the
      <strong>
        <a href="https://en.wikipedia.org/wiki/Separation_of_concerns">separation of concerns</a>
      </strong>. Good software keeps separate things separate and loosely-coupled so that it's easy to change one without breaking the other. One typical separation that you will see in nearly every information system is the separation between data and the way those data are presented on-screen.</p>
    <p>By separating our data from the way they are presented, we achieve a number of benefits:</p>
    <ul>
      <li>Several HTML pages can all share the same CSS file, allowing us to change the look of our entire site by editing only one file.</li>
      <li>The same HTML can be presented to different users in different ways. For example, you could allow users to "skin" the site in one of several different themes, or you could use different default formatting for different regions of the world with different aesthetic sensibilities</li>
      <li>You can also dynamically adjust the look of your page by applying new style rules to elements in response to user interaction (clicking, hovering, scrolling, etc.)</li>
    </ul>
    <h2 id="sec-addingastylesheettoyourpage">Adding a Stylesheet to your Page</h2>
    <p>You can add CSS rules to your page in three different ways, but the best practice is to use a separate CSS stylesheet file, and link that to your HTML page. These stylesheet files are typically put into a
      <code>css/</code> subdirectory to keep them separate from all your HTML pages.</p>
    <p>For example, say you wanted to create one stylesheet for your
      <code>index.html</code> page that you created during the
      <a href="../encoding-html/">Encoding in HTML Tutorial</a>. Create a new directory named
      <code>css</code> next to your
      <code>index.html</code> file. Then create a new file in that new
      <code>css/</code> directory named
      <code>styles.css</code>. The resulting file structure should be like this:</p>
    <pre><code>myproject/
|-- index.html
|-- css/
    |-- styles.css</code></pre>
    <p>Now link this new stylesheet to your HTML file by adding the following element to the
      <code>&lt;head&gt;</code> section of your page:</p>
    <pre><code class="html language-html">&lt;head&gt;
    ...existing elements...
    &lt;link rel="stylesheet" href="css/styles.css"&gt;
&lt;/head&gt;</code></pre>
    <p>The name you use for your CSS file can be anything you want. It just needs to match what you put in the
      <code>href</code> attribute of the
      <code>&lt;link&gt;</code> element.</p>
    <h2 id="sec-normalizingbrowserdefaults">Normalizing Browser Defaults</h2>
    <p>Each web browser applies a set of default styles to any page it loads, which you can then override in your stylesheets. Unfortunately, these defaults are not completely consistent across all the browsers, so you might end up with a slightly different look in Edge than in Chrome.</p>
    <p>Thankfully the web development community has responded by creating
      <a href="https://necolas.github.io/normalize.css/">normalize.css</a>. This is a CSS stylesheet that simply normalizes all of the browser defaults to one consistent set of base styles, so that you can build upon a solid, uniform, and well-known foundation.</p>
    <p>Unless you are using a CSS framework that already includes the normalize.css rules (e.g., Bootstrap), it's good practice to include normalize.css as the first stylesheet you link to from your page. The link to your stylesheet comes after, so that your rules will build upon those defined in normalize.css.</p>
    <p>The easiest way to load normalize.css into your page is via
      <a href="https://cdnjs.com/libraries/normalize">their online version</a>:</p>
    <pre><code class="html language-html">&lt;head&gt;
    &lt;!-- normalize.css --&gt;
    &lt;link rel="stylesheet" href="https://cdnjs.cloudflare.com/ajax/libs/normalize/5.0.0/normalize.min.css"&gt;

    &lt;!-- our own stylesheet --&gt;
    &lt;link rel="stylesheet" href="css/styles.css"&gt;
&lt;/head&gt;</code></pre>
    <p>Files that end with
      <code>.min.css</code> have been "minified," which just means that all the spaces have been taken out to make the file smaller and faster to download. When loading a library file from an online location like this, always use the
      <code>.min.css</code> version if it's available.</p>
    <p>This works well in every case except when you are offline, which can happen when you are doing development. If you need access to normalize.css while offline,
      <a href="https://necolas.github.io/normalize.css/5.0.0/normalize.css">download it</a> to your project folder and link to it using a relative path, just like the link to your own stylesheet. By convention, we typically put files like this into a
      <code>lib/</code> folder, to keep them separate from our own files that we actively change.</p>
    <h2 id="sec-basicsyntax">Basic Syntax</h2>
    <p>A CSS stylesheet follows this basic syntax:</p>
    <pre><code class="css language-css">/* this is a comment */
selector {
    property: value;
    property: value;
}

selector {
    property: value;
    property: value;
}</code></pre>
    <p>A stylesheet is a series of
      <strong>rules</strong>. A rule starts with a
      <strong>selector</strong>, which specifies which elements the rule applies to. The selector is followed by a pair of braces, inside of which is a set of formatting properties. Each formatting property consists of a name and a value. The property name is separated from the property value by a colon, and each property/value pair must end with a semi-colon. Don't forget the semi-colon! If you do, the browser will likely ignore that formatting property.</p>
    <p>Like most programming languages, multiple whitespace characters are coalesced into one, so the layout of the rule can vary. For example, you can start the braces on the line below the selector, or you can put everything all on one line if you want. But most developers will use the style shown above, as it's easy to read and maintain. For large CSS files, professionals will typically
      <strong>minify</strong> them before deploying them to a production server, which involves removing all the whitespace to make the file smaller.</p>
    <p>You can add comments to your stylesheets by wrapping them with
      <code>/*</code> and
      <code>*/</code>. Comments are ignored by the browser, but can be very helpful for explaining things to other developers (or for reminding yourself about why you did something in a particular way).</p>
    <h2 id="sec-selectors">Selectors</h2>
    <p>There are
      <a href="http://www.w3schools.com/cssref/css_selectors.asp">many kinds of selectors</a> supported in CSS, but you will use only a handful in most situations.</p>
    <h3 id="sec-elementselectors">Element Selectors</h3>
    <p>Element selectors select elements by their element name. For example, this selector will select all
      <code>&lt;p&gt;</code> elements in the page, regardless of where they exist.</p>
    <pre><code class="css language-css">p {
    ...
}</code></pre>
    <h3 id="sec-classselectors">Class Selectors</h3>
    <p>Class selectors select elements by the value of their
      <code>class</code> attribute. Every HTML element can have a
      <code>class</code> attribute set to one or more class names. Multiple class names are separated by a space, and for obvious reasons, your class names can't contain spaces. For example, this paragraph element has two class names:
      <code>alert</code> and
      <code>alert-warning</code>.</p>
    <pre><code class="html language-html">&lt;p class="alert alert-warning"&gt;...&lt;/p&gt;</code></pre>
    <p>After you add a class attribute to an element, you can then refer to that class in your CSS using a class selector. Class selectors look like this:</p>
    <pre><code class="css language-css">/* selects all elements with class="alert" */
.alert {
    ...
}

/* selects all elements with class="alert-warning" */
.alert-warning {
    ...
}

/* selects all elements with class="alert alert-warning"
(i.e., elements that have both classes) */
.alert.alert-warning {
    ...
}</code></pre>
    <p>Note the
      <code>.</code> character at the start of the name. That tells the browser that what follows is a class name and not an element name. But remember that the
      <code>.</code> character is used only in the CSS rule; in your HTML
      <code>class</code> attribute, you use the class name without the preceding
      <code>.</code> character.</p>
    <p>CSS rules are also additive. If you attach multiple style classes to the same element (like we did above), the browser will combine all formatting properties from all style rules in the order that they are defined. So in the case above, because the paragraph has both the
      <code>alert</code> and
      <code>alert-warning</code> style classes, the browser will combine all formatting properties from the
      <code>.alert</code> and
      <code>.alert-warning</code> rules and apply all of them to the element. If the same property is defined in both rules, the later one will override the earlier one.</p>
    <p>Multiple elements within your page can all share common class names, so you can define style classes for common UI components that you use in several places. For example, say you are writing a tutorial and you want to style all new terms you introduce in a particular way. You could define a rule in your stylesheet with the selector
      <code>.term</code>, setting various formatting properties you want to apply to new terms (e.g., bold, yellow background color, etc). </p>
    <pre><code class="css language-css">.term {
    font-weight: bold;
    background-color: yellow;
}</code></pre>
    <p>When you introduce a new term in your HTML page, simply add the
      <code>term</code> style class to an element that wraps around the term:</p>
    <pre><code class="html language-html">&lt;p&gt;A stylesheet is a series of &lt;span class="term"&gt;rules&lt;/span&gt;.
A rule starts with a &lt;span class="term"&gt;selector&lt;/span&gt;, ...&lt;/p&gt;</code></pre>
    <h3 id="sec-idselectors">ID Selectors</h3>
    <p>ID selectors select elements by their
      <code>id</code> attribute. Every HTML element can have an
      <code>id</code> attribute, but unlike the
      <code>class</code> attribute, the value of the
      <code>id</code> attribute must be unique within the page. That is, no two elements can have the same value for their
      <code>id</code> attributes. Thus, the
      <code>id</code> attribute is less flexible than the
      <code>class</code> attribute, and should be used only when you really need to refer to a specific element and only that one element.</p>
    <p>ID selectors start with a
      <code>#</code> sign:</p>
    <pre><code class="css language-css">/* selects the element with id="my-id" */
#my-id {
    ...
}</code></pre>
    <p>Since the
      <code>id</code> attribute is commonly used to create bookmark hyperlinks (i.e., links that scroll the page to a particular section of the page), id selectors are a handy way to style those elements.</p>
    <h3 id="sec-descendantselectors">Descendant Selectors</h3>
    <p>All the selectors mentioned so far will select all matching elements regardless of where they are in the HTML element tree. But sometimes you want to style only a set of elements that exist within a particular parent or ancestor element, and not all the other matching elements elsewhere in the page. You can do this kind of targeted selecting using a descendant selector.</p>
    <p>For example, say you wanted to style all paragraphs within the
      <code>&lt;header&gt;</code> element, but not the other paragraph elements that exist elsewhere in the page. You can select just those paragraph elements using a selector like this:</p>
    <pre><code class="css language-css">/* selects only the p elements contained within header elements */
header p {
    ...
}</code></pre>
    <p>The syntax is a space-delimited list of selectors, so you can use element or class selectors at each level. You can also have as many levels as you want, but it's best practice to use no more than two or three. For example, to target all elements with the style class
      <code>.logo</code> that exist inside a paragraph that exists inside the header, your selector would look like:</p>
    <pre><code class="css language-css">/* selects elements with class="logo"
contained within p elements
contained within header elements */
header p .logo {
    ...
}</code></pre>
    <p>Note that descendant selectors will select matching descendant elements anywhere lower in the tree branch, not just direct children, so the
      <code>.logo</code> elements here could be nested several layers below the
      <code>&lt;p&gt;</code> element. This is usually a good idea because you may introduce new nesting layers to your page as you go along. But if you really want to select only direct children, you can use this variant of the syntax, known as a
      <strong>child selector</strong>:</p>
    <pre><code class="css language-css">/* selects only the p elements that are direct children the header element  */
header &gt; p {
    ...
}</code></pre>
    <h3 id="sec-groupselectors">Group Selectors</h3>
    <p>Sometimes you want to apply the same formatting to several elements, all of which might have different element names or style classes. For example, you might want to set the same font-weight on all the
      <code>h1</code>,
      <code>h2</code>, and
      <code>h3</code> elements. Instead of defining three separate rules, you can use one rule with a group selector:</p>
    <pre><code class="css language-css">/* selects all h1, h2, and h3 elements  */
h1, h2, h3 {
    font-weight: 300;
}</code></pre>
    <p>The syntax is a list of selectors separated by commas. You can use element, class, or even descendant selectors for each element in the list.</p>
    <p>Group selectors are an excellent way to adhere to the
      <strong>DRY principle</strong>:
      <strong>D</strong>on't
      <strong>R</strong>epeat
      <strong>Y</strong>ourself! If you find yourself copying and pasting formatting properties between rules, stop and ask yourself if you can use a group selector instead.</p>
    <h3 id="sec-hoverandfocuspseudoselectors">Hover and Focus Pseudo Selectors</h3>
    <p>The last types of selectors that you will use quite often are the hover and focus
      <strong>pseudo-selectors</strong>. These select elements that the mouse is hovering over, or that have the keyboard focus, respectively. You can use these to apply different formatting on hover or focus, usually to indicate that the element is clickable, or to help those with vision impairments notice where the keyboard focus is. The syntax looks like this:</p>
    <pre><code class="css language-css">/* selects all elements with class="nav-link" */
.nav-link {
    color: white;
}

/* selects all elements with class="nav-link" that are being hovered over with the mouse */
.nav-link:hover {
    /* change color to red */
    color: red;
}

/* selects all &lt;input&gt; elements that have keyboard focus */
input:focus {
    /* change background color to yellow */
    background-color: yellow;
}</code></pre>
    <h3 id="sec-otherselectors">Other Selectors</h3>
    <p>These are the most common ones you will use, but there are many other kinds of powerful selectors you also have at your disposal for less-common tasks. See the
      <a href="http://www.w3schools.com/cssref/css_selectors.asp">CSS Selectors Reference</a> for a complete list.</p>
