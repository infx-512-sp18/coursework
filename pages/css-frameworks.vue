    <h1>CSS frameworks</h1>

    <p>In the
      <a href="../adding-style/">Adding Some Style</a> and
      <a href="../responding-device/">Responding to the Device</a> tutorials, you learned how to write style rules that change the appearance of your web pages. Although you could implement a entire site with only custom style rules, most professionals build upon a well-tested
      <strong>CSS framework</strong> instead. The framework normalizes browser default styling, applies consistent and attractive formatting to all HTML elements, and defines several style classes you can use for common UI components (badges, alerts, cards, responsive navigation bars, tabs, drop-down buttons, tool tips and popovers, sliders, switches, carousels, etc.). You can then add your own custom rules on top of the framework to tweak the default framework formatting.</p>
    <p>A CSS framework is just a stylesheet with a bunch of rules that someone else wrote for you, and some accompanying JavaScript for the interactive features. There's nothing magic about it. You can look at the stylesheet and see all the rules that are defined in there, and it's all stuff you could have written yourself. But those rules have been crafted by professionals and tested on a wide array of browsers to ensure consistent results, so it's a good idea to build on top of them.</p>
    <h2 id="sec-popularcssframeworks">Popular CSS Frameworks</h2>
    <p>There are several popular CSS frameworks to choose from. All of them provide beautiful formatting of HTML elements, pre-defined responsive multi-column layout grids that work on all browsers, and all the common UI elements you see on most web sites.</p>
    <h3 id="sec-bootstrap">Bootstrap</h3>
    <p>The most commonly-used CSS framework on the web is
      <a href="https://getbootstrap.com/">Bootstrap</a>. It was originally created at Twitter to enforce some consistency amongst their internal tools, but after they released it as an open-source project in 2011, it became
      <em>very</em> widely used. That wide use has benefits and drawbacks: it's very well tested, documented, and supported, but it's also so prevalent that it's default look has become clich√©. Thankfully, it's rather easy to
      <a href="https://getbootstrap.com/docs/4.0/getting-started/theming/">customize Bootstrap</a> with your own fonts, colors, sizing, etc.</p>
    <h3 id="sec-foundation">Foundation</h3>
    <p>Bootstrap's chief rival is
      <a href="https://foundation.zurb.com/">Foundation</a>. It has the reputation of being more ahead-of-the-curve than Bootstrap, introducing new features sooner. They were the first framework to use a responsive mobile-first design, and their new version 6 already offers a
      <a href="https://foundation.zurb.com/sites/docs/flex-grid.html">flexbox-based responsive grid</a>. Foundation has mostly the same UI elements as Bootstrap, but with a different default look at feel, which can also be
      <a href="https://foundation.zurb.com/sites/download.html/#customizeFoundation">customized</a> to match your own branding.</p>
    <h3 id="sec-materialdesignlite">Material Design Lite</h3>
    <p>Bootstrap and Foundation defined their own visual design language, but if you are a fan of Google's
      <a href="https://material.google.com/">Material Design</a> instead, there are a few CSS frameworks based upon that. The official Google implementation is known as
      <a href="https://getmdl.io/">Material Design Lite</a>, or MDL for short. Material Design is
      <em>very opinionated</em> so MDL is difficult to customize beyond changing the primary and accent colors. Their style class names are also
      <em>very verbose</em>, as they follow the
      <a href="https://en.bem.info/methodology/">Block, Element, Modifier</a> (BEM) naming method. But it's a well-implemented framework that has all the smarts and money of Google behind it.</p>
    <h3 id="sec-materialize">Materialize</h3>
    <p>The other popular Material Design implementation is
      <a href="http://materializecss.com/">Materialize</a>. This is an open-source project, so it's not supported by Google,
      <a href="https://getmdl.io/faq/#existing-implementations">nor does Google feel that it's a "correct" implementation</a> of Material Design. Regardless, a lot of people use it, primarily because it's easy to learn if you are coming from Bootstrap.</p>
    <h2 id="sec-addingaframeworktoyourpage">Adding a Framework to your Page</h2>
    <p>Each framework has a "getting started" page that describes your various options for adding the framework to your HTML page. Nearly all of them will let you link to their production files on a Content Delivery Network (CDN), download them as a zip file, or add them to your project via a package manager like
      <code>npm</code> or
      <code>bower</code>. Each method has its benefits and drawbacks.</p>
    <h3 id="sec-linkingtoacdn">Linking to a CDN</h3>
    <p>Linking to a CDN is a very easy option that has several benefits. The first is
      <strong>simplicity</strong>. For example, linking to Bootstrap's version 3.3.7 files on a CDN is as simple as adding these elements to the
      <code>&lt;head&gt;</code> section of your web page:</p>
    <pre><code class="html language-html">&lt;!-- Bootstrap v3.3.7 CSS stylesheet --&gt;
&lt;link rel="stylesheet" href="https://maxcdn.bootstrapcdn.com/bootstrap/3.3.7/css/bootstrap.min.css" integrity="sha384-BVYiiSIFeK1dGmJRAkycuHAHRg32OmUcww7on3RYdg4Va+PmSTsz/K68vbdEjh4u" crossorigin="anonymous"&gt;

&lt;!-- jQuery library (required by Bootstrap's JavaScript library) --&gt;
&lt;script src="http://code.jquery.com/jquery-3.1.0.min.js" integrity="sha256-cCueBR6CsyA4/9szpPfrX3s49M9vUU5BgtiJj06wt/s=" crossorigin="anonymous"&gt;&lt;/script&gt;

&lt;!-- Bootstrap v3.3.7 JavaScript library --&gt;
&lt;script src="https://maxcdn.bootstrapcdn.com/bootstrap/3.3.7/js/bootstrap.min.js" integrity="sha384-Tc5IQib027qvyjSMfHjOMaLkfuWVxZxUPnCJA7l2mCWNIpG9mGCD8wGNIcPD7Txa" crossorigin="anonymous"&gt;&lt;/script&gt;</code></pre>
    <div class="alert alert-warning">Note that this loads version 3.3.7 specifically. Always check the
      <a href="http://getbootstrap.com/getting-started/#download-cdn">Bootstrap Getting Started page</a> to get the elements for their most current version.</div>
    <p>Simplicity is one benefit, but
      <strong>download speed</strong> is another. Content Delivery Networks are a set of web servers that can deliver commonly-requested content very quickly all over the world. CDNs replicate their content to machines in several regions of the world, and use dynamic Domain Name Service (DNS) resolution to steer users to the machine nearest them. So a user in Australia might download the Bootstrap CSS from a server located in Singapore, while a user in France might get the same CSS file from a server located in Ireland. Although it may seem like the Internet is instantaneous, dragging files halfway around the world is still relatively slow. If the files are large, it can create a noticeable delay.</p>
    <p>Another reason that CDNs increase download performance is
      <strong>browser caching</strong>. If multiple sites all use the CDN version of Bootstrap, then the browser only has to download that file the first time you visit one of those sites. The browser can then reuse the previously downloaded file for all other sites that link to the same URL. With popular frameworks like Bootstrap, it's highly likely that your user has already visited a site that links to Bootstrap's CDN version, and thus the Bootstrap CSS and JavaScript are already in the user's browser cache.</p>
    <p>Another benefit is
      <strong>dynamic patching</strong>. If the developers of your framework discover a bug that can be patched without breaking existing code, they can re-release the patched file to the CDN, and your users' will automatically pick it up the next time they visit your site.</p>
    <p>The only drawback of linking to a CDN is that it won't work when you are offline. If you commonly do your development offline, or if you are building a web application that is meant to run offline, you must download the CSS framework files into your project directory using one of the other two methods.</p>
    <h3 id="sec-downloadingazip">Downloading a Zip</h3>
    <p>The second method for adding a CSS framework is downloading the files as a zip and unpacking them into your project directory. As noted above, one primary benefit of this approach is that the framework files will be
      <strong>available even when you are offline</strong>. </p>
    <p>A second potential benefit is that framework sites often let you customize the contents of the zip, adjusting fonts, colors, etc. For example, Bootstrap's
      <a href="http://getbootstrap.com/customize/">customize page</a> allows you to select only the components you need, and adjust base styling properties before downloading your customized zip file.</p>
    <p>A third benefit is realized if you use the
      <a href="http://sass-lang.com/">Sass</a> or
      <a href="http://lesscss.org/">Less</a> CSS pre-compilers. These tools extend the CSS syntax to include features found in most other programming languages: variables, functions, inheritance, includes, mix-ins, etc. Most CSS frameworks are built using one of these two pre-compilers, and their
      <strong>source files are commonly included in the downloaded zip</strong>. You can then refer to these source files directly in your own Sass or Less code, including only the parts of the framework you actually plan on using. You can also override their standard fonts, colors, sizes, etc., simply by resetting their variables.</p>
    <p>A fourth benefit is realized if you use a build system like gulp or webpack. It's common to link your HTML page to both your CSS framework's stylesheet and a few of your own, but this will result in one network round-trip per CSS file. If you download the framework's stylesheet, you can concatenate it with your own stylesheets, creating only one CSS file to download. This can enhance the page load experience, especially on slower mobile networks.</p>
    <p>There are two main drawbacks to this method. First, it
      <strong>adds large framework files to your code repository</strong> that could easily be downloaded from the web as needed. Second, these
      <strong>zips typically don't include other libraries that the framework depends upon</strong>. For example, Bootstrap's JavaScript library requires the jQuery library, but that's not included in their distribution zip file, as the jQuery library is maintained by a different set of people. To eliminate both of these drawbacks, use a package manager.</p>
    <h3 id="sec-usingapackagemanager">Using a Package Manager</h3>
    <p>Package managers are command-line programs that consult online directories of available packages, find all other packages a given package depends upon, and download all of them to a subdirectory within your project directory. They also commonly record the set of packages your program is using in a special file that you can add to your project's code repository. This takes the guess-work out of determining the set of packages you need for a given framework, and makes it really simple for a new developer joining your project to get all the packages your project needs in order to run.</p>
    <p>In the web development space, the most commonly-used package managers are
      <a href="https://www.npmjs.com/">
        <code>npm</code>
      </a> and
      <a href="https://bower.io/">
        <code>bower</code>
      </a>. The former was originally created for the server-side Node.js environment, while the latter was a fork of the former, adjusted for the special needs of client-side web development projects. Since then,
      <code>npm</code> has expanded to include both server and client-side packages, so these days you can use either in most circumstances.</p>
    <p>Let's see how we can use
      <code>bower</code> to install the Bootstrap CSS framework. Bower is installed using the
      <code>npm</code> package manager, so to install bower, you first need to
      <a href="https://nodejs.org/en/">install Node.js</a>, which installs
      <code>npm</code>. Once you have Node.js, install bower by running this at your command prompt from any directory:</p>
    <pre><code class="bash language-bash">$ sudo npm install -g bower</code></pre>
    <blockquote>
      <p>
        <strong>Windows Users</strong>: you don't need the
        <code>sudo</code> part of that command. Just execute
        <code>npm install -g bower</code>
      </p>
    </blockquote>
    <p>Bower uses the
      <a href="https://git-scm.com/downloads">git command line tools</a> to clone packages from their source repositories, so if you don't already have the git command line tools installed, you need to install them as well. You can verify if you have the git command line tools installed by running this command:</p>
    <pre><code class="bash language-bash">$ git --version</code></pre>
    <p>If you get a command not found error, you need to
      <a href="https://git-scm.com/downloads">install the tools</a>.</p>
    <p>To use
      <code>bower</code> in a project, you first run this command
      <strong>within your project directory</strong> to create the file it uses to track meta-data about your project, including the packages your project depends on:</p>
    <pre><code class="bash language-bash">$ bower init</code></pre>
    <p>You can accept all the default values by hitting
      <code>Enter</code> after each prompt. After all the prompts are answered, it creates a file named
      <code>bower.json</code> to capture all the meta-data about your project. You should add, commit, and push this file to your code repository.</p>
    <p>You can then install Bootstrap's files and record your dependency upon it using this command:</p>
    <pre><code class="bash language-bash">$ bower install --save bootstrap</code></pre>
    <p>This will download both the Bootstrap and jQuery packages (because Bootstrap depends on jQuery) to a new directory named
      <code>bower_components</code>. You can then include them in your page by adding these elements to your
      <code>&lt;head&gt;</code> section:</p>
    <pre><code class="html language-html">&lt;!-- Bootstrap css file --&gt;
&lt;link rel="stylesheet" href="bower_components/bootstrap/dist/css/bootstrap.min.css"&gt;
&lt;!-- jQuery JavaScript library --&gt;
&lt;script defer src="bower_components/jquery/dist/jquery.min.js"&gt;&lt;/script&gt;
&lt;!-- Bootstrap JavaScript library --&gt;
&lt;script defer src="bower_components/bootstrap/dist/js/bootstrap.min.js"&gt;&lt;/script&gt;</code></pre>
    <p>Note that the URLs in the
      <code>href</code> attributes are no longer absolute URLs pointing to a CDN. Instead they are relative URLs pointing to a sub-directory named
      <code>bower_components</code>. This directly will need to be on your web server so that the server can send these files to the user's browser. </p>
    <p>When new developers join your project, they can quickly install all the packages listed in the
      <code>bower.json</code> file, as well as all of their dependencies, using this one command:</p>
    <pre><code class="bash language-bash">$ bower install</code></pre>
    <p>Because this simple command will install the packages on-demand, it's customary to add the
      <code>bower_components</code> directory to your
      <code>.gitignore</code> file. That way the files in that directory won't be added to your repository. The only exception to this rule is if you are using GitHub pages to host your site. If so, the
      <code>bower_components</code> directory must be added to your repo so that GitHub pages can serve the files to the browser.</p>
    <p>You can also use
      <code>bower</code> to upgrade packages, or remove them from your project. See the
      <a href="https://bower.io/docs/api/">bower documentation</a> for more details.</p>
    <h3 id="sec-whichisbestcdnordownload">Which is Best? CDN or Download?</h3>
    <p>If you ask a group of developers which is the better approach&mdash;linking to a CDN or downloading via a package manager&mdash;you'll probably start a very heated debate. It's kind of like the
      <a href="https://www.youtube.com/watch?v=SsoOG6ZeyUI">spaces
        <em>v.</em> tabs</a> debate. Each method has its benefits and drawbacks, and neither option is clearly superior to the other. But choose you must, and your choice should be something you stick with. The one thing you don't want to do is mix the two approaches in one project.</p>
    <p>If you are new to web development and can't decide which approach to use, link to the CDN version. It's simple and the lack of offline support probably won't affect you much.</p>
    <h2 id="sec-responsivelayoutgrids">Responsive Layout Grids</h2>
    <p>At the core of all of these popular CSS Frameworks is a responsive grid layout system. These grids allow you to build multi-column layouts on all browsers, even those that don't support the new flexbox standard. These grids use very complicated CSS syntax to achieve this effect, and can still end-up with rendering problems, but they are your only option if you need to provide multi-column layout to IE users.</p>
    <p>These grid systems divide the horizontal line into 12 equal units. A given element can span as many of these grid units as you want, but after 12 units, the next element will wrap to the next line. Since 12 can be divided evenly by 6, 4, or 3, you can easily create layouts with 2, 3, or 4 equally-sized columns that sit next to each other on the same line. Or you can create one column that spans 4 units and another that spans 8 units. Any combination that adds up to 12 is fine.</p>
    <p>The number of grid units any given element consumes is specified using specially-named CSS style classes. For example, this markup uses Bootstrap to create three equally-sized columns on all screens:</p>
    <pre><code class="html language-html">&lt;div class="container"&gt;
    &lt;div class="row"&gt;
        &lt;div class="col-xs-4"&gt;
            &lt;h2&gt;Column 1&lt;/h2&gt;
            &lt;p&gt;Lorem ipsum dolor sit amet, consectetur adipisicing elit.&lt;/p&gt;
        &lt;/div&gt;
        &lt;div class="col-xs-4"&gt;
            &lt;h2&gt;Column 2&lt;/h2&gt;
            &lt;p&gt;Lorem ipsum dolor sit amet, consectetur adipisicing elit.&lt;/p&gt;
        &lt;/div&gt;
        &lt;div class="col-xs-4"&gt;
            &lt;h2&gt;Column 3&lt;/h2&gt;
            &lt;p&gt;Lorem ipsum dolor sit amet, consectetur adipisicing elit.&lt;/p&gt;
        &lt;/div&gt;
    &lt;/div&gt;
&lt;/div&gt;</code></pre>
    <p>In Bootstrap, grids should always be put inside an element with
      <code>class="container"</code> or
      <code>class="container-fluid"</code>. Both of these style classes add a little padding to the left and right, and the
      <code>container</code> class sets the
      <code>width</code> property so that it doesn't grow ridiculously wide on large desktop monitors.</p>
    <p>The
      <code>&lt;div class="row"&gt;</code> element starts a new row, and each direct child
      <code>&lt;div&gt;</code> within that row element defines a new column. The style class
      <code>col-xs-4</code> tells Bootstrap that the element should consume 4 grid units on extra-small screens and larger (essentially, all screens), so all three of those
      <code>&lt;div&gt;</code> elements will appear side-by-side on all screen sizes. Here's what it looks like in the browser:</p>
