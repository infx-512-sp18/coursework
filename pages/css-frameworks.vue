    <h2 id="sec-uicomponents">UI Components</h2>
    <p>Besides a responsive grid system, CSS Frameworks like Bootstrap also offer a host of useful
      <a href="http://getbootstrap.com/components/">UI components</a>. For example, adding a Bootstrap alert to your page requires this simple markup:</p>
    <pre><code class="html language-html">&lt;div class="alert alert-danger" role="alert"&gt;Danger Will Robinson, Danger!&lt;/div&gt;</code></pre>
    <div class="alert alert-danger">Danger Will Robinson, Danger!</div>
    <p>The
      <code>role="alert"</code> attribute is included to help screen readers know that this element is something more than a generic
      <code>&lt;div&gt;</code> element. The
      <code>role</code> attribute is defined in the
      <a href="https://www.w3.org/TR/wai-aria/roles#role_definitions">Aria accessibility standard</a>, and it's good practice to add this attribute to elements that play a role that is different from what the element name normally suggests.</p>
    <p>To allow users to dismiss an alert, just extend your markup like this:</p>
    <pre><code class="html language-html">&lt;div class="alert alert-warning alert-dismissible" role="alert"&gt;
  &lt;button type="button" class="close" data-dismiss="alert" aria-label="Close"&gt;&lt;span aria-hidden="true"&gt;&amp;times;&lt;/span&gt;&lt;/button&gt;
  Danger Will Robinson, Danger!
&lt;/div&gt;</code></pre>
    <div class="alert alert-warning alert-dismissible" role="alert">
      <button type="button" class="close" data-dismiss="alert" aria-label="Close">
        <span aria-hidden="true">&times;</span>
      </button>
      Danger Will Robinson, Danger!
    </div>
    <p>Just like the
      <code>role</code> attribute, the
      <code>aria-label</code> and
      <code>aria-hidden</code> attributes are defined in the Aria accessibility standard. The former provides a pronounceable label for the close button, and the latter tells the screen reader to ignore the
      <code>&amp;times;</code> HTML entity, as it won't be able to describe it in any sensible way.</p>
    <p>Try experimenting with other
      <a href="http://getbootstrap.com/components/">Bootstrap UI Components</a> using my
      <a href="http://codepen.io/drstearns/pen/XKoyEN">Bootstrap Playground CodePen</a>. Just copy/paste the markup from Bootstrap's documentation to see what it creates.</p>
    <h2 id="sec-largerapplicationcomponents">Larger Application Components</h2>
    <p>Bootstrap offers all kinds of simple UI components like these, but it also has some more complex ones that are useful in web applications. For example, you can add a modal dialog to your page and open it from a button:</p>
    <pre><code class="html language-html">&lt;!-- button to open modal --&gt;
&lt;p&gt;
    &lt;button type="button" class="btn btn-primary btn-lg" data-toggle="modal" data-target="#myModal"&gt;
      Open Modal Dialog
    &lt;/button&gt;
&lt;/p&gt;

&lt;!-- modal dialog markup, hidden until opened --&gt;
&lt;div class="modal fade" id="myModal" tabindex="-1" role="dialog" aria-labelledby="myModalLabel"&gt;
    &lt;div class="modal-dialog" role="document"&gt;
        &lt;div class="modal-content"&gt;
            &lt;div class="modal-header"&gt;
