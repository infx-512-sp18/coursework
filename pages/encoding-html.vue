    <p>Now that you understand the basic HTML syntax, let's see how they combine to create a basic page. As we noted earlier, every page begins with the HTML5 doctype declaration, and the rest is a tree of elements containing content. The root element is named
      <code>&lt;html&gt;</code> and it should contain two child elements:
      <code>&lt;head&gt;</code> and
      <code>&lt;body&gt;</code>. Add this to your page:</p>
    <pre><code class="html language-html">&lt;!DOCTYPE html&gt;
&lt;html lang="en"&gt;
&lt;head&gt;

&lt;/head&gt;
&lt;body&gt;

&lt;/body&gt;
&lt;/html&gt;</code></pre>
    <h3 id="sec-theheadsection">The
      <code>&lt;head&gt;</code> Section</h3>
    <p>The
      <code>&lt;head&gt;</code> element contains information about the page that doesn't appear in the main content window when viewing the page in the browser. For example, we can specify the title of the page here:</p>
    <pre><code class="html language-html">&lt;!DOCTYPE html&gt;
&lt;html lang="en"&gt;
&lt;head&gt;
    &lt;title&gt;My Web Page&lt;/title&gt;
&lt;/head&gt;
&lt;body&gt;

&lt;/body&gt;
&lt;/html&gt;</code></pre>
    <p>Most browsers will show the page title in the tab at the top of the browser window, and use that as the default bookmark name if you bookmark the page. But search indexers and screen readers for the blind also use this to get the page title, which is often a strong signal about what the page body is about.</p>
    <p>Note that I indented the
      <code>&lt;title&gt;</code> element so that it visibly looks like it's contained within the
      <code>&lt;head&gt;</code> element. This is good practice, and will help you read your page as the structure gets more complex. Since
      <code>&lt;html&gt;</code> can only contain
      <code>&lt;head&gt;</code> and
      <code>&lt;body&gt;</code> we don't typically indent those, but every other element should be indented one level farther than the element that contains it.</p>
    <p>Another element you commonly see the in the
      <code>&lt;head&gt;</code> section is
      <code>&lt;meta&gt;</code>, which can be used to specify lots of different meta-data about the page. One very important bit of meta-data is the character set used when saving this file. The character set tells the browser how to translate the bits it receives from the server (or the local disk) into letters. Nearly all editors these days will save files in the UTF-8 character set, as it allows the mixing of different scripts (Latin, Cyrillic, Chinese, Arabic, etc) in the same file, while still optimizing the file's size. To tell the browser that this file was saved in UTF-8, add the following
      <code>&lt;meta&gt;</code> element to the
      <code>&lt;head&gt;</code> section:</p>
    <pre><code class="html language-html">&lt;!DOCTYPE html&gt;
&lt;html lang="en"&gt;
&lt;head&gt;
    &lt;meta charset="UTF-8"&gt;
    &lt;title&gt;My Web Page&lt;/title&gt;
&lt;/head&gt;
&lt;body&gt;

&lt;/body&gt;
&lt;/html&gt;</code></pre>
    <p>You can also use the
      <code>&lt;meta&gt;</code> element to specify other meta-data, such as the author, description, and keyword information about your document:</p>
    <pre><code class="html language-html">&lt;!DOCTYPE html&gt;
&lt;html lang="en"&gt;
&lt;head&gt;
    &lt;meta charset="UTF-8"&gt;
    &lt;meta name="author" content="your name"&gt;
    &lt;meta name="description" content="description of your page"&gt;
    &lt;meta name="keywords" content="list,of,keywords,separate,by,commas"&gt;
    &lt;title&gt;My Web Page&lt;/title&gt;
&lt;/head&gt;
&lt;body&gt;

&lt;/body&gt;
&lt;/html&gt;</code></pre>
    <p>Again, these are not visible in the browser window, but programs like search indexers refer to these elements to gather meta-data about your page.</p>
    <p>Another important
      <code>&lt;meta&gt;</code> element to define is the one that controls how a browser on a mobile device handles zooming of the page. By default, a mobile browser will attempt to zoom out to show the entire page when it first loads. This is necessary if the page doesn't adjust its layout for smaller screens, but
      <a href="http://www.w3schools.com/css/css_rwd_viewport.asp">if your page is responsive to the screen width</a> (which ours will be), you can tell the browser not to zoom out using the
      <code>&lt;meta name="viewport"&gt;</code> element:</p>
    <pre><code class="html language-html">&lt;!DOCTYPE html&gt;
&lt;html lang="en"&gt;
&lt;head&gt;
    &lt;meta charset="UTF-8"&gt;
    &lt;meta name="viewport" content="width=device-width, initial-scale=1.0"&gt;
    &lt;meta name="author" content="your name"&gt;
    &lt;meta name="description" content="description of your page"&gt;
    &lt;meta name="keywords" content="list,of,keywords,separate,by,commas"&gt;
    &lt;title&gt;My Web Page&lt;/title&gt;
&lt;/head&gt;
&lt;body&gt;

&lt;/body&gt;
&lt;/html&gt;</code></pre>
    <h3 id="sec-thebodysection">The
      <code>&lt;body&gt;</code> Section</h3>
    <p>The
      <code>&lt;body&gt;</code> element contains all the content that is visible on the page. There are
      <a href="http://www.w3schools.com/tags/ref_byfunc.asp">many elements</a> you can use here to convey your document's content. For now, let's just add a heading and a paragraph:</p>
    <pre><code class="html language-html">&lt;!DOCTYPE html&gt;
&lt;html lang="en"&gt;
&lt;head&gt;
    &lt;meta charset="UTF-8"&gt;
    &lt;meta name="viewport" content="width=device-width, initial-scale=1.0"&gt;
    &lt;meta name="author" content="your name"&gt;
    &lt;meta name="description" content="description of your page"&gt;
    &lt;meta name="keywords" content="list,of,keywords,separate,by,commas"&gt;
    &lt;title&gt;My Web Page&lt;/title&gt;
&lt;/head&gt;
&lt;body&gt;
    &lt;h1&gt;Hello World!&lt;/h1&gt;
    &lt;p&gt;This is a paragraph in the page.&lt;/p&gt;
&lt;/body&gt;
&lt;/html&gt;</code></pre>
    <p>You can now open this page in your web browser. Double click on the HTML file in your Finder/File Explorer, and you should see the heading and the paragraph displayed in the browser window. The page title should also be shown in the browser tab.</p>
