    <h2>Course structure</h2>
    <p>The course is divided into
      <a href="https://canvas.uw.edu/courses/1202083/assignments/syllabus#infx512-schedule">ten modules</a>, each lasting a week. We start by learning how to
      <a href="../managing-files/">manage our files</a> using the version control system git, and the collaborative cloud repository service GitHub. Then we learn how to encode information into HTML, how to add some style to those pages, how to make them responsive to the device, and how to leverage existing CSS frameworks. After the first half of the course, you will have developed the skills necessary to build attractive, engaging, and responsive web sites.</p>
    <p>During the second half of the course, you will learn how to use JavaScript to build interactive web applications. After learning the basics of the JavaScript language, you'll discover how to dynamically modify your web pages in response to user interactions. You'll also learn how to create HTML content from data, and fetch that data dynamically from a web service.</p>
    <p>The last week of the course lets you apply all of the skills you've learned to a project of your own choosing. This could be a personal portfolio site, or a website for an organization you volunteer with, or a new tool you've been wanting to build. This project may be done individually, or in teams.</p>
    <p>For each of these modules, you will read a few tutorials, occasionally watch a demonstration video, and then complete a challenge. The challenge allows you to practice and demonstrate the skills you learned in the tutorials, while building and publishing a web site or application you could include in your portfolio.</p>
